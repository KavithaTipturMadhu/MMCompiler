float sqrt(float val)
{
  float x = val/10;

  float dx;

  float diff;
  float min_tol = 0.00001f;

  int i, flag;

  flag = 0;
  if (val == 0 ) x = 0;
  else {
    for (i=1;i<20;i++)
      {
	if (!flag) {
	  dx = (val - (x*x)) / (2.0f * x);
	  x = x + dx;
	  diff = val - (x*x);
	  if (diff <= min_tol) flag = 0;
	}
	else 
	  x = x ;
      }
  }
  return (x);
}

void redefine_start(void)
{
  float x=1234.0f;
  sqrt(x);
}
